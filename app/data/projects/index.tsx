// Updated organizedData function to handle multiple categories and return an array
export const organizedData = (): TOrganizedData[] => {
  const categories: ProjectCategory[] = [
    'web-development',
    'python-ai-ml',
    'ui-ux',
    'backend',
    'other',
  ];

  const organized: TOrganizedData[] = categories.map(category => ({
    title: categoriesData[category].title,
    icon: categoriesData[category].icon,
    description: categoriesData[category].description,
    items: projectsData.filter(project => project.categories.includes(category)),
  }));

  return organized;
};
